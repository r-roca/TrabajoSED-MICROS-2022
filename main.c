void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM2)
	{
		 uint16_t adc_value = HAL_ADC_GetValue(&hadc1);

		 if (adc_value > 500)
		 {
		      // 3 //Incrementa la variable que mide el tiempo que se mantiene pulsado el botón
		      tiempo_pulsado++;

		      // 2 // Si el botón acaba de ser pulsado (estado anterior era no pulsado)
		      if (botton_on == 0)
		      {
		           //Establece el estado del botón a pulsado
		           botton_on = 1;
		           if (tiempo_no_pulsado >= TIME_BETWEEN_LETTERS)
		           {
		        	   traducir_y_display(v_morse);
		        	   strcpy(v_morse, "");
		           }
		           tiempo_no_pulsado = 0;
		      }
		 }
		      // Si el botón no está pulsado (valor del convertidor A/D es menor que el umbral)
		 else
		 {
			 // 1 //Incrementa la variable que mide el tiempo que no se mantiene pulsado el botón
			 tiempo_no_pulsado++;

		     // 4 //Si el botón acaba de dejar de ser pulsado (estado anterior era pulsado)
		   	 if (botton_on == 1)
		   	 {
		         // Establece el estado del botón a no pulsado
		         botton_on = 0;
		         if(tiempo_pulsado <= TIEMPO_PUNTO)
		         {
		        	 strcat(v_morse, ".");
		         }
		         if(TIEMPO_PUNTO < tiempo_pulsado <=TIEMPO_RAYA)
		         {
		          	 strcat(v_morse, "-");
		         }
		         tiempo_pulsado = 0;
		   	  }
		 }
	}
}





void traducir_y_display(char* code)
{
	//recorrer la lookup table de morse
	for(int i = 0; i < strlen(morse_codes) / strlen(morse_codes[0]); i++)
	{
		// si se ha encontrado una coincidencia, mostrar letra
		if(strcmp(code, morse_codes[i]) == 0)
				{
					display_char('A' + i);
					return;
				}
		else display_char('?'); //no se ha encontrado coincidencia
	}
	return 0;
}




void display_char(char c)
{
	lcd_print(&lcd, c);
	lcd_scrollDisplayRight(&lcd);
	HAL_Delay(300);

	return 0;
}






