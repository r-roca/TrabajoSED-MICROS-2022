void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if((HAL_GetTick()-counter) >= periodo_comprobacion){ // debouncer

		if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // if the interrupt source is channel1
		{
			if (Is_First_Captured==0) // if the first value is not captured
			{
				IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read the first value
				Is_First_Captured = 1;  // set the first captured as true

				/*
				if(HAL_GetTick() - siguiente_letra > 5000 && caracter > 0){ // si es true
					traducir_display(v_morse);// insertar nueva letra para resetear el vector de morse;
				}*/
			}

			else   // if the first is already captured
			{
				IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // read second value

				if (IC_Val2 > IC_Val1)
				{
					Difference = IC_Val2-IC_Val1;
				}

				else if (IC_Val1 > IC_Val2)
				{
					Difference = (0xffffffff - IC_Val1) + IC_Val2;
				}

				//float refClock = TIMCLOCK/(PRESCALAR);
				float mFactor = 1;

				usWidth = Difference*mFactor;

				__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter
				Is_First_Captured = 0; // set it back to false

				siguiente_letra = HAL_GetTick(); // comprobar cu치ndo fue la ultima vez que se levant칩 el pulsador

				// solo si est치 tapado el ldr, es cuando est치 en modo lectura
				if(tapado == 1){
					vectorMorse(usWidth); // mandar tiempo a la funcion
				}

			}
		}

		counter=HAL_GetTick();
	}
}


void vectorMorse(uint32_t tiempo)
{
	if (tiempo <= 300000)
	{
		 strcat(v_morse, ".");
	}
	else {
		strcat(v_morse, "-");
	}
	caracter++;
}


void traducir_display(char* code)
{
	//recorrer la lookup table de morse
	for(int i = 0; i < 26; i++) //
	{
		// si se ha encontrado una coincidencia, mostrar letra
		if(strcmp(code, morse_codes[i]) == 0)
				{
					//imprimir(abecedario[i]); // codigo anterior
					insertarLetra(abecedario[i]);
			     	return;
				}
	}

	// si no encuentra un match reinicia el vector de morse
	for (int i = 0; i < 5; i++) {
	    v_morse[i] = '\0';
	}

	return;
}

